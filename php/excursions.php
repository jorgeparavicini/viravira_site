function getOrderedExcursions(mysqli $conn)
{
    $sql = "SELECT * FROM excursion";
    $result = $conn->query($sql);

    $excursions = [];
    while ($row = $result->fetch_assoc()) {
        $type = $row['type'];
        if (!array_key_exists($type, $excursions)) {
            $excursions[$type] = [$row];
        } else {
            array_push($excursions[$type], $row);
        }
    }

    return $excursions;
}

